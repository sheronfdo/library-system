    public void fillTable(String sql) throws ClassNotFoundException, SQLException {
        subModel = (DefaultTableModel) subTable.getModel();
        subModel.setRowCount(0);
        if (sql == null) {
            sql = "select * from subject where status = 1";
        }
        ResultSet rs = DBConnect.getFromDB(sql);
        while (rs.next()) {
            Vector v = new Vector();
            v.add(rs.getInt("subID"));
            v.add(rs.getString("description"));
            v.add(rs.getInt("status"));

            subModel.addRow(v);
        }

    }

