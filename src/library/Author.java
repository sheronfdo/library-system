    public void fillTable(String sql) throws ClassNotFoundException, SQLException {
        aurModel = (DefaultTableModel) autTable.getModel();
        aurModel.setRowCount(0);
        if (sql == null) {
            sql = "select * from author where status = 1";
        }
        ResultSet rs = DBConnect.getFromDB(sql);
        while (rs.next()) {
            Vector v = new Vector();
            v.add(rs.getInt("aurID"));
            v.add(rs.getString("name"));
            v.add(rs.getString("address"));
            v.add(rs.getString("telephoneNo"));
            v.add(rs.getString("email"));
            v.add(rs.getString("nic"));
            v.add(rs.getString("dob"));
            v.add(rs.getString("gender"));
            v.add(rs.getInt("status"));

            aurModel.addRow(v);
        }

    }

